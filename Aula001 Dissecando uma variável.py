a = input('Digite algo')
print('É um digito?', a.isdigit())
print('O texto está em caixa alta?', a.isupper())
print('É alpha-numérico?', a.isalnum())
print('É um caracter especial? :)', a.isascii())
print('É numérico apenas?', a.isnumeric())
print('É algum tipo de digito?', a.isdigit())
print('É um número decimal?', a.isdecimal())
print('É algum tipo de identificador?', a.isidentifier())
print('Está em letras minúsculas apenas?', a.islower())
print('Da para imprimir isso?', a.isprintable())
print('É apenas espaços?', a.isspace())
print('Está "capitalizada"? Isso é, letras maiúsculas e minúsculas', a.istitle())

